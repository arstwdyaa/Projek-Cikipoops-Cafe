                <p><b>Coding untuk Web Development</b></p>
                <p>Anda juga mungkin sudah tahu bahwa coding adalah bagian dari web development. Web development atau web programming sendiri 
                    adalah proses pembuatan sebuah website atau aplikasi dengan coding. Website-website inilah yang nantinya akan dikunjungi oleh 
                    orang-orang yang memiliki akses internet. Web development sendiri memang terbagi menjadi dua :</p>
                <p>1. Back-End Development</p>
                <p>Back-end artinya adalah ‘bagian belakang’, yang merujuk pada sisi website yang tidak dapat dilihat oleh pengunjung atau pengguna .</p>
                <p>2. Front-End Development</p>
                <p>Front-end development adalah istilah yang digunakan untuk membahas bagian interaksi yang terjadi di ‘muka’ dari sebuah website 
                    seperti fungsionalitas tombol, scroll, interaksi pada klik, dan sebagainya.</p>
                <p>3. Web Design</p>
                <p>Seperti namanya, web design merujuk pada proses pengembangan aset visual dari sebuah website. Web designer seringkali bekerjasama 
                    dengan front-end developer untuk memastikan ide desain mereka diaplikasikan dengan tepat pada sebuah website.</p>
                <p>Selain itu, kedua bagian proyek ini bisa juga dilakukan oleh satu orang yang sama, bertanggung jawab untuk mengatur fungsionalitas website dan 
